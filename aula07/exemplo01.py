                          # Listas - 

# São variáveis compostas para armazenamento de mais informaçoes por vez
# É caracterizada no python pel sinal []
# Por covenção usamos o nome das listas no plural. É opcional o prefixo 'list'

list_meses = ['janeiro', 'fevereiro', 'março', 'abril', 'maio', 'junho', 'julho']
print(list_meses)

list_meses = ['janeiro', 'fevereiro', 'março', 'abril', 'maio', 'junho', 'julho']
print(list_meses[0]) # primeiro
print(list_meses[-1]) # ultimo
print(list_meses[-2]) # penultimo...
print(list_meses[1:4]) # imprime a partir do indice 1 ate o 3
print(list_meses[2:]) # imprime a partir do indice 2 em diante
print(list_meses[:5]) # imprime do inicio ate o 4
print(list_meses[0:6:2]) # imprime da posição pulando uma casa ate o 5
print(list_meses[0:7:3]) # imprime da posição pulando duas casas ate o 6

